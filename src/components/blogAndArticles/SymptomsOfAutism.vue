                            говорят такую обратную связь: «Мне не о чем разговаривать с друзьями на
                            перемене. Они глупые, постоянно в телефоне что-то смотрят, играют в
                            игры какие-то. А я сидел один, мне не хотелось с ними общаться, я не знал
                            о чем. Мне не интересно. Обычно я разгадываю кроссворды или с одним
                            другом могу общаться по поводу школьного предмета». Напоминает
                            маленького старичка. Или другой пример «Мои друзья сказали, что не хотят
                            со мной дружить, зачем ты пришел? Я не знал что ответить, просто
                            продолжал стоять рядом».
