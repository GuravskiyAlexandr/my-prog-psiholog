                            слова, не в контексте, «не в тему» как говорится. Или у ребенка с РАС
                            вновь появившиеся слова вдруг исчезают, появляются новые и снова
                            исчезают. Никогда не произносит слова по просьбе, а только когда сам
                            захочет или спонтанно. Наблюдается отраженная речь: эхолалия. Ребенок
                            просто повторяет за окружающими вопрос, вместо ответа. Отражает слова,
                            темп речи, интонацию. Говорит слова во втором или третьем лице.
                            «Хочешь пить?» вместо «Хочу пить».
                            Даже у более старших деток при высоко функциональном аутизме и у
                            взрослых с аутизмом наблюдаем явление нарушения семантико-
                            прагматической стороны речи. Неумение адекватно пользоваться речью
                            для общения. Непонимание контекста ситуации.
