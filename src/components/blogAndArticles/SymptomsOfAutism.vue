                        <h2 class="title">
                            6) Нарушение поведения. Повторяющееся поведение – очень часто встречающийся симптом РАС.
                        </h2>
                        <p></p>
                        <p style="margin-bottom: 3px">
                            Это негибкость поведения, застреваемость на определенных действиях.
                            Один и тот же интерес, игра одна и та же, надев шапку не дает снять ее в
                            помещении, привыкает к определенной пище, отвергает другие продукты. С
                            криком заходит в помещение и с таким же криком из него выходит. Это
                            трудности перехода с одного вида деятельности на другой вид. В МКБ 10
                            этот критерий описан как нарушение реципрокной деятельности.
                            Стремление к повторяющемуся поведению тесно связано со склонностью
                            легко образовывать стойкие привычки. Мозг аутистического ребенка
                            настолько не загружен социальными связями, что он вынужден
                            поддерживать свою психическую активность и заполнять поведение
                            повторяющимися действиями, что и составляют его скудный поведенческий
                            репертуар.</p>
                        <div>Повторяющееся поведение называют:</div>
                        <ul class="subtitle-2 font-weight-bold">
                            <li> -аутостимуляция;</li>
                            <li> -стереотипия;</li>
                            <li> -компульсии;</li>
                            <li> -ритуалы.</li>
                        </ul>
                        <p></p>
                        <p>
                            Есть самостоятельное расстройство, которое
                            называется компульсивное расстройство и оно отличается от компульсий у
                            ребенка с РАС тем, что человек
                            страдающий компульсивным расстройством сохраняет критику к своей
                            проблеме и идет сам за помощью к специалистам. Он просто не может
                            побороть желание совершить какое-то повторяющееся действие. При
                            аутизме критики нет, может повторять сложные ритуалы, иногда в эти
                            ритуалы вовлечены все члены семьи и если кто-то из близких отказывается
                            участвовать в ритуалах, вызывает протест. Стереотипии – повторяющееся
                            поведение, или сенсорное поведение, потому что это склонность
                            совершать какие-то действия, которые приносят ребенку какие-то приятные
                            сенсорные ощущения и застревает на них. Это не целенаправленные
                            действия, они не переходят в навык. Это хлопанье в ладоши, покачивания
                            из стороны в сторону, кручения на месте, бег по диагонали, по кругу,
                            потряхивания, перебирание пальцами перед глазами, хождение на
                            носочках, привычка перелистывать книги и не важно какие, листание чего
                            угодно и при попытке родителей внести осознанный игровой компонент или
                            попытки взаимодействовать вызывают только протест – это должно
                            настораживать. Вмешаться в процесс, навязаться в качестве игрового
                            партнера вызывает бурный протест, также вариант повторяющегося
                            поведения челночный бег с одного конца комнаты в другой, увлечение
                            мелкими предметами, разрывание газет, выкладывание в ряд игрушек др.
                            Часто привлекает не предмет, а его свойства, например, играет не с

                            машинкой, а крутит колесики длительное время лежа на боку. Что это
                            значит? Эти особенности к повторяющемуся сенсорному поведению можно
                            объяснить особенностями мозга к центральному связыванию. Это значит,
                            что ребенок неспособен на интегративное целостное восприятие мира.
                            Социальное предназначение той или иной игрушки, и он зацикливается
                            только на определенной характеристике этого предмета. Играет со
                            свойствами предмета. Так он достигает сенсорного удовольствия.
                            Повторяющееся поведение проявляется часто и может заполнять все
                            времяпровождение ребенка. Как только ребенок предоставлен сам себе он
                            сразу же погружается в эти сенсорные манипуляции. Репертуар
                            повторяющегося поведения разный у разных детей. Например, лепет, он
                            предшествует речи в возрасте 4-5 месяцев. При аутизме мозг застревает
                            на приятных ощущениях от лепета или в 2 -3 года начинает лепетать,
                            говорить на своем «птичьем языке». За этим стоит сенсорное поведение,
                            что мешает развиться нормативному речевому развитию. В 2 -3 года начал
                            лепетать и постоянно лепечет. Чаще это сенсорная стимуляция.
                            Как видно проявлений спектра очень много. Все вышеперечисленные
                            проблемы поведения характерны для расстройств аутистического спектра,
                            могут варьироваться у разных детей, могут зависеть от тяжести
                            расстройства и от поведения окружающих в ответ, важно помогает ли
                            семья и есть ли компетентные специалисты.
