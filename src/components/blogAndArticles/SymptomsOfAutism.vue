                            сверстник обиделся и сказал, что не будет играть, а ребенок почему-то
                            бежит за ним и требует, навязывает свое присутствие, например, сверстник
                            может толкнуть в ответ и родителей это огорчает, что ребенок не понимает
                            контекста и не знает, как себе вести. Может быть так что ребенок
                            пристально наблюдает за поведением других детей, и родители думают,
                            что это интерес к другим детям, но дальше наблюдения это не идет и даже
                            если он подойдет к детям, то адекватного акта социального
                            взаимодействия не происходит. Ударил, толкнул, отобрал, укусил и это
                            есть взаимодействие. Скорее это доступ к желаемому, а не
                            взаимодействие. Дети с аутизмом — это дети что очень хотят общаться и
